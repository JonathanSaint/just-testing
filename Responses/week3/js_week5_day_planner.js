/*
Scenario:
A planner app shows activities based on the day of the week.

Problem to Solve:
Use a switch statement to display the correct day.
*/

let day = 4;

switch (day) {
    case 1 :
        console.log("Reading.");
        break;
    case 2 :
        console.log("Watching OuterBanks.")
        break;
    case 3 :
        console.log("Watching StrangerThings.")
        break;
    case 4 :
        console.log("Frontend Development.")
        break;
    case 5 :
        console.log("Writing some plans down.")
        break;
    case 6 :
        console.log("Soccer")
        break;
    case 7 :
        console.log("Going to church.")
        break;
}